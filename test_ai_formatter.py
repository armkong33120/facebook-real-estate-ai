import os
import google.generativeai as genai
import config

# ใช้ API Key จากไฟล์ config.py ของคุณ
genai.configure(api_key=config.GEMINI_API_KEY)

def ai_format_post(raw_text):
    """
    ฟังก์ชันส่งข้อความไปให้ AI จัดระเบียบให้อ่านง่าย
    """
    model = genai.GenerativeModel('gemini-2.5-flash-lite')
    
    prompt = f"""
    คุณคือผู้ช่วยเอเจนท์อสังหาริมทรัพย์ หน้าที่ของคุณคือ 'จัดระเบียบ' ข้อความต่อไปนี้ให้เป็นโพสต์ Facebook ที่อ่านง่าย
    
    กฎเหล็ก:
    1. ห้ามเพิ่มเนื้อหา ห้ามแต่งเรื่องใหม่ ห้ามเปลี่ยนราคา หรือข้อมูลสำคัญ
    2. ใช้ Bullet points เพื่อแยกหัวข้อ (เช่น รายละเอียดห้อง, ราคา, จุดเด่น)
    3. ใส่ Emoji ที่เหมาะสมเพื่อให้โพสต์ดูน่าสนใจ (แต่อย่าเยอะเกินไป)
    4. จัดลำดับให้อ่านง่ายที่สุด โดยมีชื่อโครงการอยู่บนสุด
    
    นี่คือข้อความดิบ:
    ---
    {raw_text}
    ---
    
    ตอบกลับมาเฉพาะข้อความที่จัดระเบียบเสร็จแล้วเท่านั้น (ไม่ต้องมีคำเกริ่น)
    """
    
    response = model.generate_content(prompt)
    return response.text.strip()

